#include <unistd.h>
#include <stdio.h>

int	insert_nbr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			write(1, "Error!\n", 7);
	}
	return (0);
}
